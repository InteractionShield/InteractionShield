import ca.mcgill.sable.util.*;

/**
 * A (non-null) reference value literal.
 * <p>
 * Can only appear in a BirState (i.e., not in the source of a transition 
 * system).
 */

public class RefLit implements Literal {

	StateVar collection;
	int index;
	Ref type;

	public RefLit(StateVar collection, int index) {
	this.collection = collection;
	this.index = index;
	this.type = new Ref(collection);
	}
	public void apply(Switch sw)
	{
		((ExprSwitch) sw).caseRefLit(this);
	}
	public StateVar getCollection() { return collection; }
	public int getIndex() { return index; }
	public Type getTargetType() { return type.getTargetType(); }
	public Type getType() { return type; }
	public String toString() {
	return collection.getName() + "#" + index;
	}
}
